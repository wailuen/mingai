/**
 * Instinct Renderer for Kailash Continuous Learning System
 *
 * Reads instincts from per-project directory and renders them as concise
 * markdown for injection into `.claude/rules/learned-instincts.md`.
 * Claude Code auto-loads this file as a regular rule file on next session.
 *
 * Target: <50 lines of output to minimize context usage.
 */

const fs = require("fs");
const path = require("path");

/**
 * Render instincts from a learning directory as concise markdown.
 *
 * @param {string} learningDir - Absolute path to per-project learning directory
 * @returns {string|null} Rendered markdown string, or null if no instincts
 */
function renderInstincts(learningDir) {
  const instinctsDir = path.join(learningDir, "instincts", "personal");
  if (!fs.existsSync(instinctsDir)) return null;

  const files = fs.readdirSync(instinctsDir).filter((f) => f.endsWith(".json"));
  if (files.length === 0) return null;

  const sections = [];
  let totalCount = 0;

  for (const file of files) {
    try {
      const category = file.replace(".json", "");
      const instincts = JSON.parse(
        fs.readFileSync(path.join(instinctsDir, file), "utf8"),
      );
      if (!Array.isArray(instincts) || instincts.length === 0) continue;

      // Filter to high-confidence instincts (>= 0.7)
      const highConfidence = instincts.filter((i) => i.confidence >= 0.7);
      if (highConfidence.length === 0) continue;

      const lines = highConfidence
        .sort((a, b) => b.confidence - a.confidence)
        .slice(0, 5) // Max 5 per category to stay concise
        .map((i) => {
          const pct = Math.round(i.confidence * 100);
          const occ = i.source?.occurrences || 0;
          const summary = summarizePattern(i.pattern, category);
          return `- ${summary} (${pct}%, ${occ} occurrences)`;
        });

      sections.push({ category: formatCategory(category), lines });
      totalCount += highConfidence.length;
    } catch {}
  }

  if (sections.length === 0) return null;

  // Build markdown
  const parts = [
    "# Learned Instincts (Auto-Generated)",
    "DO NOT EDIT - regenerated by session-end hook.",
    "",
  ];

  for (const section of sections) {
    parts.push(`## ${section.category}`);
    parts.push(...section.lines);
    parts.push("");
  }

  parts.push(
    `_${totalCount} instinct(s) across ${sections.length} category(ies). Updated: ${new Date().toISOString().split("T")[0]}_`,
  );

  return parts.join("\n");
}

/**
 * Summarize a pattern object into a human-readable one-liner.
 */
function summarizePattern(pattern, category) {
  if (!pattern) return "Unknown pattern";

  if (category === "workflow-patterns") {
    if (pattern.pattern_type) return `Use ${pattern.pattern_type} pattern`;
    return `Workflow pattern: ${JSON.stringify(pattern).substring(0, 60)}`;
  }

  if (category === "error-fixes") {
    const errorDesc = pattern.error?.error_type || "error";
    const fixDesc = pattern.fix?.fix_type || "fix applied";
    return `"${errorDesc}" -> "${fixDesc}"`;
  }

  if (category === "framework-selection") {
    if (pattern.project_type && pattern.framework) {
      return `For ${pattern.project_type} projects, use ${pattern.framework}`;
    }
    return `Framework: ${JSON.stringify(pattern).substring(0, 60)}`;
  }

  if (category === "node-usage") {
    const nodes = pattern.node_types || pattern.nodes;
    if (Array.isArray(nodes))
      return `Frequently used nodes: ${nodes.join(", ")}`;
    return `Node pattern: ${pattern.pattern_type || JSON.stringify(pattern).substring(0, 60)}`;
  }

  if (category === "dataflow-models") {
    const model = pattern.model_name || pattern.model;
    if (model) return `DataFlow model: ${model}`;
    return `DataFlow pattern: ${JSON.stringify(pattern).substring(0, 60)}`;
  }

  if (category === "test-patterns") {
    const tier = pattern.test_tier || pattern.tier;
    if (tier) return `Test tier: ${tier}`;
    return `Test pattern: ${JSON.stringify(pattern).substring(0, 60)}`;
  }

  if (category === "connection-patterns") {
    const fw = pattern.framework;
    if (fw) return `Connection pattern for ${fw}`;
    return `Connection: ${JSON.stringify(pattern).substring(0, 60)}`;
  }

  // Generic fallback: show description or pattern_type if available
  if (pattern.description) return pattern.description.substring(0, 80);
  if (pattern.pattern_type) return `Pattern: ${pattern.pattern_type}`;
  return JSON.stringify(pattern).substring(0, 70);
}

/**
 * Format a category slug into a readable heading.
 */
function formatCategory(slug) {
  return slug
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

module.exports = { renderInstincts };
