"""
TEST-037: Credential Encryption Unit Tests

Tests the vault-reference pattern for SharePoint credentials and the
HAR crypto module's encryption/decryption roundtrip.

Tier 1: Unit tests, mocking allowed, <1s timeout.

Key security invariants:
- client_secret and client_id are NEVER stored in the DB config
- Only a vault reference URI (credential_ref) is persisted
- credential_ref format: vault:mingai/{tenant_id}/{service}/{integration_id}
- HAR private keys are encrypted at rest via Fernet (derived from JWT_SECRET_KEY)
- Decrypted private keys are 64 bytes (32-byte seed + 32-byte public)
"""
import base64
import os
import re
import uuid
from unittest.mock import AsyncMock, patch

import pytest
from pydantic import ValidationError


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

TEST_JWT_SECRET = "b" * 64
TEST_TENANT_ID = "aaaaaaaa-1111-2222-3333-444444444444"
TEST_INTEGRATION_ID = "bbbbbbbb-5555-6666-7777-888888888888"

CREDENTIAL_REF_PATTERN = re.compile(r"^vault:mingai/[a-f0-9\-]+/[a-z]+/[a-f0-9\-]+$")


# ---------------------------------------------------------------------------
# SharePoint credential_ref tests
# ---------------------------------------------------------------------------


class TestCredentialRefFormat:
    """Validate the vault reference URI format used for SharePoint configs."""

    def test_vault_ref_matches_expected_pattern(self):
        """credential_ref generated by connect_sharepoint follows vault:mingai/{tid}/{svc}/{iid}."""
        vault_ref = f"vault:mingai/{TEST_TENANT_ID}/sharepoint/{TEST_INTEGRATION_ID}"
        assert CREDENTIAL_REF_PATTERN.match(
            vault_ref
        ), f"vault_ref '{vault_ref}' does not match expected pattern"

    def test_vault_ref_rejects_missing_tenant_id(self):
        """A vault ref without tenant_id segment does not match."""
        bad_ref = f"vault:mingai//sharepoint/{TEST_INTEGRATION_ID}"
        assert not CREDENTIAL_REF_PATTERN.match(bad_ref)

    def test_vault_ref_rejects_empty_service(self):
        """A vault ref with empty service segment does not match."""
        bad_ref = f"vault:mingai/{TEST_TENANT_ID}//{TEST_INTEGRATION_ID}"
        assert not CREDENTIAL_REF_PATTERN.match(bad_ref)

    def test_vault_ref_rejects_plain_secret(self):
        """A raw secret string does not match vault ref pattern."""
        raw_secret = "my-super-secret-client-key-12345"
        assert not CREDENTIAL_REF_PATTERN.match(raw_secret)


class TestSharePointConnectStoresVaultRef:
    """Verify that connect_sharepoint stores credential_ref, never raw secrets."""

    def test_config_contains_credential_ref_not_secrets(self):
        """
        The config dict built by connect_sharepoint must contain
        'credential_ref' and must NOT contain 'client_id' or 'client_secret'.
        """
        from app.modules.documents.sharepoint import SharePointConnectRequest

        body = SharePointConnectRequest(
            name="HR SharePoint",
            site_url="https://company.sharepoint.com/sites/hr",
            library_name="Documents",
            client_id="real-client-id",
            client_secret="real-client-secret",
        )

        integration_id = str(uuid.uuid4())
        tenant_id = TEST_TENANT_ID

        # Reproduce the config-building logic from connect_sharepoint
        vault_ref = f"vault:mingai/{tenant_id}/sharepoint/{integration_id}"
        config = {
            "site_url": body.site_url,
            "library_name": body.library_name,
            "credential_ref": vault_ref,
        }

        assert "credential_ref" in config
        assert config["credential_ref"].startswith("vault:")
        assert "client_id" not in config
        assert "client_secret" not in config
        # The raw secret values must not appear anywhere in the config dict
        assert "real-client-id" not in str(config)
        assert "real-client-secret" not in str(config)

    def test_sharepoint_request_schema_requires_client_secret(self):
        """
        SharePointConnectRequest requires client_secret at the API boundary,
        but the route handler must strip it before DB persistence.
        """
        from app.modules.documents.sharepoint import SharePointConnectRequest

        with pytest.raises(ValidationError):
            SharePointConnectRequest(
                name="Test",
                site_url="https://company.sharepoint.com/sites/test",
                library_name="Docs",
                client_id="cid",
                # client_secret omitted -> should fail validation
            )

    def test_sharepoint_request_rejects_http_url(self):
        """site_url must start with https://."""
        from app.modules.documents.sharepoint import SharePointConnectRequest

        with pytest.raises(ValidationError) as exc_info:
            SharePointConnectRequest(
                name="Test",
                site_url="http://insecure.sharepoint.com/sites/test",
                library_name="Docs",
                client_id="cid",
                client_secret="csec",
            )
        assert "https://" in str(exc_info.value)


