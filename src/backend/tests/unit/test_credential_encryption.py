class TestHARCryptoRoundtrip:
    """Test Ed25519 keypair generation and Fernet encryption/decryption."""

    @pytest.fixture(autouse=True)
    def _set_jwt_secret(self):
        with patch.dict(os.environ, {"JWT_SECRET_KEY": TEST_JWT_SECRET}):
            yield

    def test_generate_keypair_returns_two_strings(self):
        """generate_agent_keypair returns (public_key_b64, encrypted_private_b64)."""
        from app.modules.har.crypto import generate_agent_keypair

        pub, priv_enc = generate_agent_keypair()
        assert isinstance(pub, str)
        assert isinstance(priv_enc, str)
        assert len(pub) > 0
        assert len(priv_enc) > 0

    def test_public_key_is_32_bytes_base64(self):
        """Public key decodes to exactly 32 bytes (Ed25519 raw format)."""
        from app.modules.har.crypto import generate_agent_keypair

        pub, _ = generate_agent_keypair()
        raw = base64.urlsafe_b64decode(
            pub + "=" * (4 - len(pub) % 4) if len(pub) % 4 else pub
        )
        assert len(raw) == 32

    def test_sign_and_verify_roundtrip(self):
        """Sign a payload with the encrypted private key, then verify with public key."""
        from app.modules.har.crypto import (
            generate_agent_keypair,
            sign_payload,
            verify_signature,
        )

        pub, priv_enc = generate_agent_keypair()
        payload = b"test transaction payload"
        signature = sign_payload(priv_enc, payload)

        assert isinstance(signature, str)
        assert verify_signature(pub, payload, signature) is True

    def test_verify_rejects_tampered_payload(self):
        """Verification fails if payload was modified after signing."""
        from app.modules.har.crypto import (
            generate_agent_keypair,
            sign_payload,
            verify_signature,
        )

        pub, priv_enc = generate_agent_keypair()
        payload = b"original payload"
        signature = sign_payload(priv_enc, payload)

        tampered = b"tampered payload"
        assert verify_signature(pub, tampered, signature) is False

    def test_sign_with_empty_key_raises(self):
        """sign_payload raises ValueError for empty private key."""
        from app.modules.har.crypto import sign_payload

        with pytest.raises(ValueError, match="empty"):
            sign_payload("", b"test")

    def test_get_fernet_requires_jwt_secret(self):
        """get_fernet raises ValueError when JWT_SECRET_KEY is not set."""
        from app.modules.har.crypto import get_fernet

        with patch.dict(os.environ, {"JWT_SECRET_KEY": ""}):
            with pytest.raises(ValueError, match="JWT_SECRET_KEY"):
                get_fernet()

    def test_different_jwt_secrets_produce_different_keys(self):
        """Changing JWT_SECRET_KEY produces a different Fernet key (cannot decrypt old data)."""
        from app.modules.har.crypto import generate_agent_keypair, sign_payload

        # Generate with original secret
        pub, priv_enc = generate_agent_keypair()

        # Try to sign with a different JWT_SECRET_KEY
        with patch.dict(os.environ, {"JWT_SECRET_KEY": "c" * 64}):
            with pytest.raises(ValueError, match="decrypt"):
                sign_payload(priv_enc, b"test")


