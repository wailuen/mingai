def test_platform_credential_crud(client, test_template):
    """Full CRUD: POST → GET → PUT (rotate) → DELETE → GET (excluded)."""
    tmpl_id = test_template
    hdrs = _platform_headers()
    base_url = f"/api/v1/platform/templates/{tmpl_id}/credentials"

    # POST: store a credential
    r = client.post(
        base_url,
        json={
            "key": "TEST_API_KEY",
            "value": "test-secret-crud-1234",
            "description": "CRUD test key",
            "allowed_domains": ["api.example.com"],
        },
        headers=hdrs,
    )
    assert r.status_code == 201, r.text
    body = r.json()
    assert "value" not in body, "Credential value must never be in response"
    assert body["key"] == "TEST_API_KEY"
    assert body["version"] == 1

    # GET: list keys — must show the key, not the value
    r = client.get(base_url, headers=hdrs)
    assert r.status_code == 200
    creds = r.json()["credentials"]
    found = next((c for c in creds if c["key"] == "TEST_API_KEY"), None)
    assert found is not None, "TEST_API_KEY must appear in list"
    assert "value" not in found, "Value must not be in list response"

    version = found["version"]

    # PUT: rotate with correct If-Match
    r = client.put(
        f"{base_url}/TEST_API_KEY",
        json={"value": "new-rotated-secret-5678"},
        headers={**hdrs, "If-Match": str(version)},
    )
    assert r.status_code == 200, r.text
    assert r.json()["version"] == version + 1

    # PUT: rotate with stale If-Match → 409
    r = client.put(
        f"{base_url}/TEST_API_KEY",
        json={"value": "should-fail-conflict"},
        headers={**hdrs, "If-Match": str(version)},  # stale version
    )
    assert r.status_code == 409, "Stale If-Match must return 409"

    # DELETE: soft-delete (no active agents in this test)
    r = client.delete(f"{base_url}/TEST_API_KEY", headers=hdrs)
    assert r.status_code == 200, r.text
    assert r.json().get("retention_until") is not None

    # GET after delete: key excluded from active list
    r = client.get(base_url, headers=hdrs)
    assert r.status_code == 200
    creds = r.json()["credentials"]
    assert not any(c["key"] == "TEST_API_KEY" for c in creds), "Deleted key must not appear in list"


