def test_health_endpoint_completeness(client, test_template):
    """Health endpoint returns correct completeness status and per-key status."""
    tmpl_id = test_template
    hdrs = _platform_headers()
    base_url = f"/api/v1/platform/templates/{tmpl_id}/credentials"
    key_name = "HEALTH_TEST_KEY"

    # Ensure clean
    asyncio.run(_run_sql(
        "DELETE FROM platform_credential_metadata WHERE template_id = :tid AND key = :key",
        {"tid": tmpl_id, "key": key_name},
    ))

    # Health when missing
    r = client.get(f"{base_url}/health", headers=hdrs)
    assert r.status_code == 200
    health = r.json()
    assert health["status"] in ("incomplete", "not_required")

    # Store the credential
    r = client.post(
        base_url,
        json={"key": "TEST_API_KEY", "value": "health-test-secret", "allowed_domains": ["api.example.com"]},
        headers=hdrs,
    )
    # May already exist; either 201 or 409 is acceptable
    assert r.status_code in (201, 409)

    # Health when complete
    r = client.get(f"{base_url}/health", headers=hdrs)
    assert r.status_code == 200
    health = r.json()
    # All required keys should now be stored
    assert health.get("status") in ("complete", "incomplete")  # depends on prior state

    # Cache-Control must prevent caching of credential data
    assert "no-store" in r.headers.get("cache-control", "").lower()
