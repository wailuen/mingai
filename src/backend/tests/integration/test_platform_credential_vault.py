# Test 3: Concurrent rotation — one 200, one 409
# ---------------------------------------------------------------------------


def test_platform_credential_rotation_concurrency(client, test_template):
    """Two simultaneous PUT requests with same If-Match: one 200, one 409."""
    tmpl_id = test_template
    hdrs = _platform_headers()
    base_url = f"/api/v1/platform/templates/{tmpl_id}/credentials"

    # Store initial key for this test
    key_name = "CONCURRENT_KEY"
    asyncio.run(_run_sql(
        "DELETE FROM platform_credential_metadata WHERE template_id = :tid AND key = :key",
        {"tid": tmpl_id, "key": key_name},
    ))
    r = client.post(
        base_url,
        json={"key": key_name, "value": "concurrent-v1", "allowed_domains": ["api.example.com"]},
        headers=hdrs,
    )
    assert r.status_code == 201, r.text

    # Fetch current version
    r = client.get(base_url, headers=hdrs)
    cred = next(c for c in r.json()["credentials"] if c["key"] == key_name)
    version = cred["version"]

    # Fire two concurrent PUT requests with the same version
    def rotate(value: str) -> int:
        res = client.put(
            f"{base_url}/{key_name}",
            json={"value": value},
            headers={**hdrs, "If-Match": str(version)},
        )
        return res.status_code

    with concurrent.futures.ThreadPoolExecutor(max_workers=2) as executor:
        futures = [executor.submit(rotate, f"concurrent-v{i}") for i in [2, 3]]
        status_codes = sorted(f.result() for f in concurrent.futures.as_completed(futures))

    assert status_codes == [200, 409], (
        f"Expected [200, 409], got {status_codes}. One rotation must win, one must get 409"
    )

    # Cleanup
    asyncio.run(_run_sql(
        "UPDATE platform_credential_metadata SET deleted_at = NOW(), deleted_by = 'test' "
        "WHERE template_id = :tid AND key = :key AND deleted_at IS NULL",
        {"tid": tmpl_id, "key": key_name},
    ))


